# lists
coursesEnrolled = ['python','ruby','java']
print(coursesEnrolled)

#Accessing elements in a list
names = ["John",'Sam','Barry','Lin']
print(names)
print(names[0])
print(names[3])
print(names[-1])
print(names[-3])

#List slicing
print(names)
print(names[1:3])
print(names[2:])

#Updating lists
marks = [56,76,69,71,39]
marks[4] = 43
print(marks)

#Deleting lists
names = ["John",'Sam','Barry','Lin']
print(names)
del names[1]
print(names)

# deleting whole list
del names
